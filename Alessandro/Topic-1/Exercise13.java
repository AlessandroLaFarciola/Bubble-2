public class Exercise13 {
    public static void main(String[] args) {
        int x = 5;
        double y = 2.5;

        System.out.println(x + " + " + y + " = " + (x + y));
        System.out.println(x + " - " + y + " = " + (x - y));
        System.out.println(x + " * " + y + " = " + (x * y));
        System.out.println(x + " / " + y + " = " + (x / y));
    }
}