public class Exercice_1_3 {
    public static void main(String[]args){
        //+
        int x = 8;
        int y = 2;
        int result1 = x + y;
        System.out.println("The result of x + y = " + result1);

        //-
        int result2 = x - y;
        System.out.println("The result of x - y = " + result2);
        //*
        int result3 = x * y;
        System.out.println("The result of x * y = " + result3);

        ///
        double result4 = x / y;
        System.out.println("The result of x / y = " + result4);
    }
}
